    public void mostrar(int id){
        ah4DAORelacional ahd = new ah4DAORelacional();
        sucursalesPOO sp = ahd.obtenerSucu(id);
        codsucu.setText(sp.getId()+"");
        nomsucu.setText(sp.getNombre()+"");
        dirsucu.setText(sp.getDireccion()+"");
        corsucu.setText(sp.getCorreo()+"");
        telsucu.setText(sp.getTelefono()+"");
    }
    
    public void actualizar(){
        ah4DAORelacional ahd = new ah4DAORelacional();
        sucursalesPOO sp = new sucursalesPOO(Integer.parseInt(codsucu.getText()),nomsucu.getText(),dirsucu.getText(),
                corsucu.getText(),Integer.parseInt(telsucu.getText()));
        ahd.modificarSucursal(sp);
        principal pr = new principal();
        pr.setVisible(true);
        dispose();
    }
