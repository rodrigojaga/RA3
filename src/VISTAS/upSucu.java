    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(nomsucu.getText().isEmpty()||nomsucu.getText().isEmpty()||corsucu.getText().isEmpty()
            ||telsucu.getText().isEmpty()){

            JOptionPane.showMessageDialog(null, "DEBE LLENAR Y/O SELECCIONAR TODOS LOS CAMPOS");
            Color ca = new Color(255,160,160);
            if(nomsucu.getText().equals("")){
                nomsucu.setBackground(ca);
            }
            if(dirsucu.getText().equals("")){
                dirsucu.setBackground(ca);
            }
            if(corsucu.getText().equals("")){
                corsucu.setBackground(ca);
            }
            if(telsucu.getText().equals("")){
                telsucu.setBackground(ca);
            }
        }else{
            actualizar();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

