    public void enviar(){
        ah4DAORelacional ahd = new ah4DAORelacional();
        productosPOO pp = new productosPOO(nompro.getText(),despro.getText(),Integer.parseInt(canpro.getText()),Double.parseDouble(prepro.getText()));
        ahd.crearProductos(pp);
        principal pr = new principal();
        pr.setVisible(true);
        dispose();
    }
    
