    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(nompro.getText().isEmpty()||despro.getText().isEmpty()||canpro.getText().isEmpty()
                ||prepro.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "DEBE LLENAR Y/O SELECCIONAR TODOS LOS CAMPOS");
            Color ca = new Color(255,160,160);
            if(nompro.getText().equals("")){
                nompro.setBackground(ca);
            }
            if(despro.getText().equals("")){
                despro.setBackground(ca);
            }
            if(canpro.getText().equals("")){
                canpro.setBackground(ca);
            }
            if(prepro.getText().equals("")){
                prepro.setBackground(ca);
            }
        }else{
            enviar();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

