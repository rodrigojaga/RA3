    public void mostrar(int id){

        ah4DAORelacional ahd = new ah4DAORelacional();
        vendedoresPOO vp = ahd.obtenerVen(id);
  
        
        if(vp.getGenero().equals("M")){
            codven.setText(String.valueOf(vp.getId()));
            nomven.setText(String.valueOf(vp.getNombre()));
            cajaven.setText(String.valueOf(vp.getCaja()));
            ventasven.setText(String.valueOf(vp.getVentas()));
            mas.setSelected(true);
            fem.setSelected(false);
            conven.setText(vp.getPassword());
  
        }else if(vp.getGenero().equals("F")){
            codven.setText(String.valueOf(vp.getId()));
            nomven.setText(vp.getNombre());
            cajaven.setText(String.valueOf(vp.getCaja()));
            ventasven.setText(String.valueOf(vp.getVentas()));
            mas.setSelected(false);
            fem.setSelected(true);
            conven.setText(vp.getPassword());
      
        }
        
        
    }
    
    private void modificar(){
        
        if(mas.isSelected() == true){
           ah4DAORelacional ahd = new ah4DAORelacional();
           vendedoresPOO vp = new vendedoresPOO(Integer.parseInt(codven.getText()),
           nomven.getText(),Integer.parseInt(cajaven.getText()),Integer.parseInt(ventasven.getText()),
           "M",conven.getText());
           ahd.modificarVendedor(vp);
           principal pr = new principal();
           pr.setVisible(true);
           dispose();
           
        }else if (fem.isSelected() == true){
           ah4DAORelacional ahd = new ah4DAORelacional();
           vendedoresPOO vp = new vendedoresPOO(Integer.parseInt(codven.getText()),
           nomven.getText(),Integer.parseInt(cajaven.getText()),Integer.parseInt(ventasven.getText()),
           "F",conven.getText());
           ahd.modificarVendedor(vp);
           principal pr = new principal();
           pr.setVisible(true);
           dispose();
            
        }
        
    }
    
