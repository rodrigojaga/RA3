    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(nomven.getText().isEmpty()||cajaven.getText().isEmpty()||ventasven.getText().isEmpty()
                ||(mas.isSelected()==false &&fem.isSelected()==false)||conven.getText().isEmpty()){

            JOptionPane.showMessageDialog(null, "DEBE LLENAR Y/O SELECCIONAR TODOS LOS CAMPOS");
            Color ca = new Color(255,160,160);
            if(nomven.getText().equals("")){
                nomven.setBackground(ca);
            }
            if(cajaven.getText().equals("")){
                cajaven.setBackground(ca);
            }
            if(ventasven.getText().equals("")){
                ventasven.setBackground(ca);
            }
            if(mas.isSelected()==false && fem.isSelected() == false){
                fem.setForeground(Color.red);
                mas.setForeground(Color.red);
            }
            if(conven.getText().equals("")){
                conven.setBackground(ca);
            }
        }else{
            enviarDatos();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

