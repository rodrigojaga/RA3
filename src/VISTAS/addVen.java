    public void enviarDatos(){
        
        if(mas.isSelected() == true){
            ah4DAORelacional ahd = new ah4DAORelacional();
            vendedoresPOO vp = new vendedoresPOO(nomven.getText(),
            Integer.parseInt(cajaven.getText()),Integer.parseInt(ventasven.getText()),
            "M",conven.getText());
            ahd.crearVendedores(vp);
            principal pr = new principal();
            pr.setVisible(true);
            dispose();
        }else if(mas.isSelected() == false){
            ah4DAORelacional ahd = new ah4DAORelacional();
            vendedoresPOO vp = new vendedoresPOO(nomven.getText(),
            Integer.parseInt(cajaven.getText()),Integer.parseInt(ventasven.getText()),
            "F",conven.getText());
            ahd.crearVendedores(vp);
            principal pr = new principal();
            pr.setVisible(true);
            dispose();
        }
    }
   
    
   
